package part2

import (
	"testing"
)

type timelineTest struct {
	input    []string
	expected int
}

var timelineTests = []timelineTest{
	{[]string{
		".......S.......",
		"...............",
		".......^.......",
		"...............",
		"......^.^......",
		"...............",
		".....^.^.^.....",
		"...............",
		"....^.^...^....",
		"...............",
		"...^.^...^.^...",
		"...............",
		"..^...^.....^..",
		"...............",
		".^.^.^.^.^...^.",
		"...............",
	}, 40},
	{[]string{
		".......S.......",
		"...............",
		".......^.......",
		"...............",
		"......^.^......",
		"...............",
		".....^.^.......",
		"...............",
	}, 7},
	{[]string{
		".......S.......",
		"...............",
		".......^.......",
		"...............",
		".......^.......",
		"...............",
	}, 2},
	{[]string{
		".......S.......",
		"...............",
		".......^.......",
		"...............",
		"........^......",
		"...............",
		".......^.^.....",
		"...............",
		"......^.^.^....",
		"...............",
	}, 10},
}

func TestCountTimelines(t *testing.T) {
	for _, test := range timelineTests {
		received := CountTimelines(test.input)
		if received != test.expected {
			t.Errorf(`%v should have been %v, was %v`, test.input, test.expected, received)
		}
	}
}
